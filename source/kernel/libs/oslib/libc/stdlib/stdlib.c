#include <ll/i386/stdlib.h>
#include <ll/unistd.h>

unsigned abs(int x)
{
    if (x < 0) return(-x);
    else return(x);
}

void exit(int code)
{
    extern void bios_restore(void);

    bios_restore();
    _exit(code);
}
